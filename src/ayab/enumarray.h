#ifndef ENUMARRAY_H_
#define ENUMARRAY_H_

template<typename V, typename E>
struct EnumArray{
    V data[static_cast<size_t>(E::_Count)];

    constexpr V operator[](const E& e) const {
      return data[static_cast<size_t>(e)];
    }
};

#endif // ENUMARRAY_H_